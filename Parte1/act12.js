// Declarar e inicializar una variable `let` dentro de un bloque
{
    let miVariableLet = "Hola Mi nombre es Sebastain";
    console.log(miVariableLet); // "Hola Mi nombre es Sebastain"
  }
  
  // Declarar e inicializar una variable `const` dentro de un bloque
  {
    const miVariableConst = "Esto es una constante";
    console.log(miVariableConst); // "Esto es una constante"
  }
  
  // Declarar e inicializar una variable `var` dentro de un bloque
  {
    var miVariableVar = "Aqui se puede cambiar el valor";
    console.log(miVariableVar); // "Aqui se puede cambiar el valor"
  }